#include "minishell.h"
#include <stdio.h>
#include <readline/readline.h>
#include <signal.h>
#include <string.h>
#include <unistd.h>

void	signals(int sig)
{
	(void)sig;
	write(1, "\n", 1);
	rl_replace_line("", 0);
	rl_on_new_line();
	rl_redisplay();
}

void	signals_control(void)
{
	signal(SIGINT, signals);
	signal(SIGQUIT, SIG_IGN);
}
